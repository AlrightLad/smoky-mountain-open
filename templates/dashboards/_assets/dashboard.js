/* ============================================================
   PARBAUGHS Orchestration Reports — Shared Renderer
   Generated by parbaughs-report-generate skill
   
   Public API:
     ParbaughsReports.render(reportType, data)
     ParbaughsReports.charts.* — chart factories
     ParbaughsReports.utils.*  — formatters
   
   Depends on: Chart.js 4.x (loaded via CDN in each HTML report)
   ============================================================ */

(function (global) {
  'use strict';

  // ---------- Token helpers (read CSS vars at runtime) ----------
  function cssVar(name) {
    return getComputedStyle(document.documentElement).getPropertyValue(name).trim();
  }

  function palette() {
    return {
      brass:  cssVar('--accent-brass'),
      moss:   cssVar('--accent-moss'),
      claret: cssVar('--accent-claret'),
      teal:   cssVar('--accent-teal'),
      amber:  cssVar('--accent-amber'),
      violet: cssVar('--accent-violet'),
      textPrimary:   cssVar('--text-primary'),
      textSecondary: cssVar('--text-secondary'),
      textTertiary:  cssVar('--text-tertiary'),
      bgCard:        cssVar('--bg-card'),
      borderSubtle:  cssVar('--border-subtle'),
    };
  }

  function chartSeriesColors() {
    return [
      cssVar('--chart-1'),
      cssVar('--chart-2'),
      cssVar('--chart-3'),
      cssVar('--chart-4'),
      cssVar('--chart-5'),
      cssVar('--chart-6'),
    ];
  }

  // ---------- Chart.js global defaults (dark theme) ----------
  function applyChartDefaults() {
    if (typeof Chart === 'undefined') return;
    const p = palette();
    Chart.defaults.color = p.textSecondary;
    Chart.defaults.borderColor = p.borderSubtle;
    Chart.defaults.font.family = '-apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, sans-serif';
    Chart.defaults.font.size = 12;
    Chart.defaults.plugins.legend.labels.color = p.textSecondary;
    Chart.defaults.plugins.tooltip.backgroundColor = p.bgCard;
    Chart.defaults.plugins.tooltip.titleColor = p.brass;
    Chart.defaults.plugins.tooltip.bodyColor = p.textPrimary;
    Chart.defaults.plugins.tooltip.borderColor = p.brass;
    Chart.defaults.plugins.tooltip.borderWidth = 1;
    Chart.defaults.plugins.tooltip.padding = 10;
  }

  // ---------- Number / time formatters ----------
  const utils = {
    fmtNumber(n) {
      if (n === null || n === undefined) return '—';
      if (Math.abs(n) >= 1e9) return (n / 1e9).toFixed(1) + 'B';
      if (Math.abs(n) >= 1e6) return (n / 1e6).toFixed(1) + 'M';
      if (Math.abs(n) >= 1e3) return (n / 1e3).toFixed(1) + 'k';
      return String(n);
    },
    fmtTokens(n) { return utils.fmtNumber(n); },
    fmtCost(n) {
      if (n === null || n === undefined) return '—';
      return '$' + n.toFixed(2);
    },
    fmtPercent(n, digits) {
      if (n === null || n === undefined) return '—';
      return (n * 100).toFixed(digits || 1) + '%';
    },
    fmtDuration(seconds) {
      if (!seconds) return '0s';
      const h = Math.floor(seconds / 3600);
      const m = Math.floor((seconds % 3600) / 60);
      if (h > 0) return `${h}h ${m}m`;
      if (m > 0) return `${m}m`;
      return `${seconds}s`;
    },
    fmtIso(iso) {
      if (!iso) return '—';
      try {
        return new Date(iso).toISOString().replace('T', ' ').slice(0, 16) + ' UTC';
      } catch { return iso; }
    },
  };

  // ---------- Chart factories ----------
  const charts = {
    doughnut(ctx, labels, values, opts) {
      opts = opts || {};
      const colors = chartSeriesColors();
      return new Chart(ctx, {
        type: 'doughnut',
        data: {
          labels: labels,
          datasets: [{
            data: values,
            backgroundColor: colors,
            borderColor: cssVar('--bg-card'),
            borderWidth: 2,
          }],
        },
        options: {
          responsive: true,
          maintainAspectRatio: false,
          cutout: '60%',
          plugins: {
            legend: { position: 'right', labels: { boxWidth: 10, padding: 8 } },
            tooltip: {
              callbacks: {
                label: function (ctx) {
                  const total = ctx.dataset.data.reduce((a, b) => a + b, 0);
                  const pct = total ? ((ctx.parsed / total) * 100).toFixed(1) : 0;
                  const fmt = opts.formatter || utils.fmtNumber;
                  return `${ctx.label}: ${fmt(ctx.parsed)} (${pct}%)`;
                },
              },
            },
          },
        },
      });
    },

    line(ctx, labels, datasets, opts) {
      opts = opts || {};
      const colors = chartSeriesColors();
      return new Chart(ctx, {
        type: 'line',
        data: {
          labels: labels,
          datasets: datasets.map((d, i) => ({
            label: d.label,
            data: d.data,
            borderColor: d.color || colors[i % colors.length],
            backgroundColor: (d.color || colors[i % colors.length]) + '22',
            borderWidth: 2,
            tension: 0.25,
            pointRadius: 3,
            pointHoverRadius: 5,
            fill: opts.fill !== false,
          })),
        },
        options: {
          responsive: true,
          maintainAspectRatio: false,
          plugins: {
            legend: { position: 'bottom', labels: { boxWidth: 10, padding: 8 } },
          },
          scales: {
            x: { grid: { color: cssVar('--border-subtle') } },
            y: {
              grid: { color: cssVar('--border-subtle') },
              ticks: { callback: opts.yFormatter || utils.fmtNumber },
              beginAtZero: opts.beginAtZero !== false,
            },
          },
        },
      });
    },

    bar(ctx, labels, datasets, opts) {
      opts = opts || {};
      const colors = chartSeriesColors();
      return new Chart(ctx, {
        type: 'bar',
        data: {
          labels: labels,
          datasets: datasets.map((d, i) => ({
            label: d.label,
            data: d.data,
            backgroundColor: d.color || colors[i % colors.length],
            borderWidth: 0,
          })),
        },
        options: {
          responsive: true,
          maintainAspectRatio: false,
          indexAxis: opts.horizontal ? 'y' : 'x',
          plugins: {
            legend: {
              display: datasets.length > 1,
              position: 'bottom',
              labels: { boxWidth: 10, padding: 8 },
            },
          },
          scales: {
            x: {
              stacked: opts.stacked || false,
              grid: { display: !opts.horizontal, color: cssVar('--border-subtle') },
              ticks: { callback: opts.horizontal ? (opts.yFormatter || utils.fmtNumber) : undefined },
            },
            y: {
              stacked: opts.stacked || false,
              grid: { display: opts.horizontal, color: cssVar('--border-subtle') },
              ticks: { callback: opts.horizontal ? undefined : (opts.yFormatter || utils.fmtNumber) },
              beginAtZero: true,
            },
          },
        },
      });
    },
  };

  // ---------- Renderers per report type ----------
  const renderers = {
    dashboard(data) {
      const p = palette();

      // Metric cards (already rendered by template; populate values)
      setText('[data-metric="weekly-tokens"]', utils.fmtTokens(data.weekly_tokens));
      setText('[data-metric="weekly-cost"]', utils.fmtCost(data.weekly_cost));
      setText('[data-metric="ships-week"]', data.ships_this_week || 0);
      setText('[data-metric="proposals-pending"]', data.proposals_pending || 0);
      setText('[data-metric="halt-count"]', data.halts_this_week || 0);
      setText('[data-metric="fiq-depth"]', data.fiq_depth || 0);

      // Budget progress bar removed (Phase 6.6) — no fictional cap.
      // Dashboards consume data.manual_quota_latest directly when needed.

      // Charts
      if (data.tokens_by_role) {
        const ctx = document.getElementById('chart-tokens-by-role');
        if (ctx) charts.doughnut(ctx, data.tokens_by_role.labels, data.tokens_by_role.values, { formatter: utils.fmtTokens });
      }
      if (data.token_trend_7d) {
        const ctx = document.getElementById('chart-token-trend');
        if (ctx) charts.line(ctx, data.token_trend_7d.labels, [{ label: 'Tokens', data: data.token_trend_7d.values, color: p.brass }], { yFormatter: utils.fmtTokens });
      }
      if (data.cycle_outcomes_7d) {
        const ctx = document.getElementById('chart-cycle-outcomes');
        if (ctx) charts.bar(ctx, data.cycle_outcomes_7d.labels, data.cycle_outcomes_7d.datasets, { stacked: true });
      }

      // Tables
      if (data.recent_handoffs) renderHandoffsTable(data.recent_handoffs);
      if (data.recent_ships) renderShipsTable(data.recent_ships);
    },

    daily(data) {
      const p = palette();
      setText('[data-metric="day-cycles"]', data.cycles_today || 0);
      setText('[data-metric="day-tokens"]', utils.fmtTokens(data.tokens_today));
      setText('[data-metric="day-cost"]', utils.fmtCost(data.cost_today));
      setText('[data-metric="day-handoffs"]', data.handoffs_today || 0);

      if (data.tokens_by_activity) {
        const ctx = document.getElementById('chart-tokens-by-activity');
        if (ctx) charts.bar(ctx, data.tokens_by_activity.labels, data.tokens_by_activity.datasets, { stacked: true, yFormatter: utils.fmtTokens });
      }
      if (data.handoffs_by_scenario) {
        const ctx = document.getElementById('chart-handoffs-by-scenario');
        if (ctx) charts.doughnut(ctx, data.handoffs_by_scenario.labels, data.handoffs_by_scenario.values);
      }
    },

    weekly(data) {
      const p = palette();
      setText('[data-metric="week-tokens"]', utils.fmtTokens(data.tokens_total));
      setText('[data-metric="week-cost"]', utils.fmtCost(data.cost_total));
      setText('[data-metric="week-ships"]', data.ships_completed || 0);
      setText('[data-metric="week-proposals"]', data.proposals_generated || 0);

      if (data.cost_trend_4w) {
        const ctx = document.getElementById('chart-cost-trend');
        if (ctx) charts.line(ctx, data.cost_trend_4w.labels, [{ label: 'Weekly cost', data: data.cost_trend_4w.values, color: p.brass }], { yFormatter: utils.fmtCost });
      }
      if (data.ships_per_week) {
        const ctx = document.getElementById('chart-ships-per-week');
        if (ctx) charts.bar(ctx, data.ships_per_week.labels, [{ label: 'Ships', data: data.ships_per_week.values, color: p.moss }]);
      }
      if (data.tokens_by_role_daily) {
        const ctx = document.getElementById('chart-tokens-by-role-daily');
        if (ctx) charts.bar(ctx, data.tokens_by_role_daily.labels, data.tokens_by_role_daily.datasets, { stacked: true, yFormatter: utils.fmtTokens });
      }
      if (data.proposals_by_lane) {
        const ctx = document.getElementById('chart-proposals-by-lane');
        if (ctx) charts.doughnut(ctx, data.proposals_by_lane.labels, data.proposals_by_lane.values);
      }
    },

    ship(data) {
      const p = palette();
      setText('[data-metric="ship-tokens"]', utils.fmtTokens(data.tokens_total));
      setText('[data-metric="ship-cost"]', utils.fmtCost(data.cost_total));
      setText('[data-metric="ship-cycles"]', data.cycles_count || 0);
      setText('[data-metric="ship-duration"]', utils.fmtDuration(data.duration_seconds));
      setText('[data-metric="ship-loc"]', data.lines_changed || 0);
      setText('[data-metric="ship-coverage"]', utils.fmtPercent(data.coverage_delta));

      if (data.tokens_by_phase) {
        const ctx = document.getElementById('chart-tokens-by-phase');
        if (ctx) charts.bar(ctx, data.tokens_by_phase.labels, data.tokens_by_phase.datasets, { stacked: true, yFormatter: utils.fmtTokens });
      }
      if (data.tokens_by_role) {
        const ctx = document.getElementById('chart-tokens-by-role');
        if (ctx) charts.doughnut(ctx, data.tokens_by_role.labels, data.tokens_by_role.values, { formatter: utils.fmtTokens });
      }
      if (data.discussion_bubbles_per_day) {
        const ctx = document.getElementById('chart-discussion-bubbles-per-day');
        if (ctx) charts.bar(ctx, data.discussion_bubbles_per_day.labels, [{ label: 'Discussion Bubbles', data: data.discussion_bubbles_per_day.values, color: p.violet }]);
      }
    },

    wave(data) {
      const p = palette();
      setText('[data-metric="wave-tokens"]', utils.fmtTokens(data.tokens_total));
      setText('[data-metric="wave-cost"]', utils.fmtCost(data.cost_total));
      setText('[data-metric="wave-ships"]', data.ships_completed || 0);
      setText('[data-metric="wave-duration"]', utils.fmtDuration(data.duration_seconds));

      if (data.ships_by_cost) {
        const ctx = document.getElementById('chart-ships-by-cost');
        if (ctx) charts.bar(ctx, data.ships_by_cost.labels, [{ label: 'Cost', data: data.ships_by_cost.values, color: p.brass }], { horizontal: true, yFormatter: utils.fmtCost });
      }
      if (data.cost_accumulation) {
        const ctx = document.getElementById('chart-cost-accumulation');
        if (ctx) charts.line(ctx, data.cost_accumulation.labels, [{ label: 'Cumulative cost', data: data.cost_accumulation.values, color: p.amber }], { yFormatter: utils.fmtCost });
      }
      if (data.code_metrics) {
        const ctx = document.getElementById('chart-code-metrics');
        if (ctx) charts.line(ctx, data.code_metrics.labels, data.code_metrics.datasets);
      }
    },

    quarterly(data) {
      const p = palette();
      setText('[data-metric="q-tokens"]', utils.fmtTokens(data.tokens_total));
      setText('[data-metric="q-cost"]', utils.fmtCost(data.cost_total));
      setText('[data-metric="q-ships"]', data.ships_completed || 0);
      setText('[data-metric="q-waves"]', data.waves_completed || 0);

      if (data.tokens_per_week) {
        const ctx = document.getElementById('chart-tokens-per-week');
        if (ctx) charts.line(ctx, data.tokens_per_week.labels, data.tokens_per_week.datasets, { yFormatter: utils.fmtTokens });
      }
      if (data.cost_by_wave) {
        const ctx = document.getElementById('chart-cost-by-wave');
        if (ctx) charts.bar(ctx, data.cost_by_wave.labels, data.cost_by_wave.datasets, { stacked: true, yFormatter: utils.fmtCost });
      }
    },
  };

  // ---------- Helpers ----------
  function setText(selector, value) {
    document.querySelectorAll(selector).forEach(el => {
      el.textContent = value;
    });
  }

  function renderHandoffsTable(rows) {
    const tbody = document.querySelector('[data-table="handoffs"] tbody');
    if (!tbody) return;
    tbody.innerHTML = rows.map(r => `
      <tr>
        <td><span class="badge neutral">${r.scenario}</span></td>
        <td>${escapeHtml(r.from)}</td>
        <td>${escapeHtml(r.to)}</td>
        <td class="text-faint">${utils.fmtIso(r.created_at)}</td>
      </tr>
    `).join('');
  }

  function renderShipsTable(rows) {
    const tbody = document.querySelector('[data-table="ships"] tbody');
    if (!tbody) return;
    tbody.innerHTML = rows.map(r => {
      const statusClass = r.status === 'complete' ? 'success' : r.status === 'halted' ? 'danger' : 'info';
      return `
      <tr>
        <td><strong>${escapeHtml(r.id)}</strong></td>
        <td>${escapeHtml(r.title || '')}</td>
        <td><span class="badge ${statusClass}">${r.status}</span></td>
        <td class="num">${utils.fmtTokens(r.tokens)}</td>
        <td class="num">${utils.fmtCost(r.cost)}</td>
      </tr>`;
    }).join('');
  }

  function escapeHtml(s) {
    if (s === null || s === undefined) return '';
    return String(s)
      .replaceAll('&', '&amp;')
      .replaceAll('<', '&lt;')
      .replaceAll('>', '&gt;')
      .replaceAll('"', '&quot;')
      .replaceAll("'", '&#039;');
  }

  // ---------- Interactivity: sortable tables ----------
  function initSortableTables() {
    document.querySelectorAll('.data-table th.sortable').forEach(th => {
      th.addEventListener('click', () => {
        const table = th.closest('table');
        const tbody = table.querySelector('tbody');
        const idx = Array.from(th.parentNode.children).indexOf(th);
        const isAsc = th.classList.contains('sort-asc');
        table.querySelectorAll('th').forEach(t => t.classList.remove('sort-asc', 'sort-desc'));
        th.classList.add(isAsc ? 'sort-desc' : 'sort-asc');
        const rows = Array.from(tbody.querySelectorAll('tr'));
        rows.sort((a, b) => {
          const av = a.children[idx]?.textContent.trim() || '';
          const bv = b.children[idx]?.textContent.trim() || '';
          const an = parseFloat(av.replace(/[^0-9.-]/g, ''));
          const bn = parseFloat(bv.replace(/[^0-9.-]/g, ''));
          if (!isNaN(an) && !isNaN(bn)) {
            return isAsc ? bn - an : an - bn;
          }
          return isAsc ? bv.localeCompare(av) : av.localeCompare(bv);
        });
        rows.forEach(r => tbody.appendChild(r));
      });
    });
  }

  // ---------- Interactivity: collapsible sections ----------
  function initCollapsible() {
    document.querySelectorAll('.collapsible').forEach(el => {
      el.addEventListener('click', () => el.classList.toggle('collapsed'));
    });
  }

  // ---------- Interactivity: theme toggle ----------
  function initThemeToggle() {
    const btn = document.querySelector('.theme-toggle');
    if (!btn) return;
    const stored = localStorage.getItem('parbaughs-reports-theme');
    if (stored) document.documentElement.setAttribute('data-theme', stored);
    btn.addEventListener('click', () => {
      const current = document.documentElement.getAttribute('data-theme') || 'dark';
      const next = current === 'dark' ? 'light' : 'dark';
      document.documentElement.setAttribute('data-theme', next);
      try { localStorage.setItem('parbaughs-reports-theme', next); } catch (e) {}
      btn.textContent = next === 'dark' ? '☀' : '☾';
    });
    btn.textContent = (document.documentElement.getAttribute('data-theme') || 'dark') === 'dark' ? '☀' : '☾';
  }

  // ---------- Public API ----------
  const ParbaughsReports = {
    render(reportType, data) {
      applyChartDefaults();
      const fn = renderers[reportType];
      if (!fn) {
        console.warn('ParbaughsReports: unknown report type', reportType);
        return;
      }
      try {
        fn(data);
      } catch (err) {
        console.error('ParbaughsReports render error:', err);
        const main = document.querySelector('main');
        if (main) {
          const div = document.createElement('div');
          div.className = 'card';
          div.innerHTML = `<div class="card-title text-danger">Render error</div><pre class="mt-3">${escapeHtml(err.message)}</pre>`;
          main.appendChild(div);
        }
      }
      initSortableTables();
      initCollapsible();
      initThemeToggle();
    },
    charts: charts,
    utils: utils,
  };

  global.ParbaughsReports = ParbaughsReports;
}(window));
